package game;

public class Microorganismo {

	public int energia;
	public int vision;
	public int velocidad;
	public int edad;
	
	// Clase Padre de Microorganismos
	
	public Microorganismo(int pEnergia, int pVision, int pVelocidad, int pEdad) {
		
		this.energia = pEnergia;
		this.vision = pVision;
		this.velocidad = pVelocidad;
		this.edad = pEdad;
		
	}
	
}
